def cal():
    num1 = int(input())
    num2 = int(input())

    operat = input()

    if(operat == "+"):
        print(num1 + num2)
    if(operat == "-"):
        print(num1 - num2)
    if(operat == "*"):
        print(num1 * num2)
    if(operat == "/"):
        print(num1 / num2)


cal()
